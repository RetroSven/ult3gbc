	set_bkg_palette( 0, 8, demonpals[0] );

	VBK_REG=1 ;
	set_bkg_data2(  0, 156U, demonframes[0] );

	if ( LCDC_REG&0x10 )
		vidbase = (unsigned char*)0x9C00 ;
	else
		vidbase	= (unsigned char*)0x9800 ;


	for ( k = 0 ; k != 13 ; k++ )
	{
		for ( j = 0 ; j != 12 ; j++ )
		{
			modpal[j+4] = ((demonpalmaps[0])[k*12 + j]) + 0x08 ;
		}

		memset( modpal, ((demonpalmaps[0])[0])+0x08, 4UL ) ;
		memset( modpal+16UL, ((demonpalmaps[0])[0])+0x08, 4UL ) ;
		set_data2(vidbase+(((UWORD)k+2)<<5L), modpal,20L) ;
	}           
