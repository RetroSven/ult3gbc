void title_animate3()
{
	UBYTE dlcv, k, j, addpal ;
	unsigned char *vidbase ;



	for ( dlcv = 0 ; dlcv != 3 ; dlcv++ )
	{

		if ( joypad() & J_START )
		{
			finishtitle = 1 ;
			break ;
		}

		if ( LCDC_REG&VIDEO_BUFFER_SECONDARY )
		{
			vidbase = (unsigned char*)0x9800 ;
			set_bkg_data2(  0, 252U, walkframes3[dlcv] );
			addpal = 0 ;
		}
		else
		{
			vidbase = (unsigned char*)0x9C00 ;
			VBK_REG = 1 ;
			set_bkg_data2(  0, 252U, walkframes3[dlcv] );
			VBK_REG = 0 ;
			addpal = 8 ;

		}




		VBK_REG = 1;  // select palette bank 



		for ( k = 0 ; k != 18 ; k++ )
		{
			for ( j = 0 ; j != 14 ; j++ )
			{
				modpaltitle[j+3] = ((walkpalmaps3[dlcv])[k*14 + j]) + addpal ;
			}

			//memset( modpaltitle, 0, 3UL ) ;
			//memset( modpaltitle+17UL, 0, 3UL ) ;
			//		memset( modpaltitle, ((walkpalmaps1[dlcv])[0])+0x00, 3UL ) ;
			//memset( modpaltitle+17UL, ((walkpalmaps1[dlcv])[0])+0x00, 3UL ) ;
			set_data2(vidbase+(((UWORD)k)<<5L), modpaltitle,20L) ;
		}           



		VBK_REG = 0;  // select palette bank 

		while ( LY_REG != 143U ) ;

		set_bkg_palette( 0, 8, walkpals[dlcv+4] );


		if ( LCDC_REG&VIDEO_BUFFER_SECONDARY )
			LCDC_REG &= VIDEO_BUFFER_PRIMARY ;	//select $9800-$9BFF
		else
			LCDC_REG |= VIDEO_BUFFER_SECONDARY ;	//select $9C00-$9FFF



	}

}
