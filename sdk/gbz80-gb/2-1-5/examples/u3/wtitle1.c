void title_animate1()
{
	UBYTE dlcv, k, j, addpal ;
	unsigned char *vidbase ;


	inside = 10 ;
	finishtitle = 0 ;

	DISPLAY_OFF ;

	for ( k = 0 ; k < 18 ; k++ )
	{
		set_data2(0x9800+(((UWORD)k)<<5L), (unsigned char*)walkmap+((UWORD)k*20L),20L) ;
		set_data2(0x9C00+(((UWORD)k)<<5L), (unsigned char*)walkmap+((UWORD)k*20L),20L) ;
	}

	VBK_REG=1 ;
	for ( k = 0 ; k < 18 ; k++ )
	{
		set_data2(0x9800+(((UWORD)k)<<5L), modflip2title,20L) ;
		set_data2(0x9C00+(((UWORD)k)<<5L), modfliptitle,20L) ;
	}
	VBK_REG=0 ;

	set_bkg_palette( 0, 8, walkpals[0] );

	if ( LCDC_REG&0x08 )
		vidbase = (unsigned char*)0x9C00 ;
	else
		vidbase	= (unsigned char*)0x9800 ;


	VBK_REG=1 ;
	set_bkg_data2(  0, 252U, walkframes1[0] );
	set_bkg_data2( 252U, 4U, blacktile ) ;



	for ( k = 0 ; k != 18 ; k++ )
	{
		for ( j = 0 ; j != 14 ; j++ )
		{
			modpaltitle[j+3] = ((walkpalmaps1[0])[k*14 + j]) + 0x08 ;
		}

		memset( modpaltitle, 0, 3UL ) ;
		memset( modpaltitle+17UL, 0, 3UL ) ;
//		memset( modpaltitle, ((walkpalmaps1[0])[0])+0x00, 3UL ) ;
		//memset( modpaltitle+17UL, ((walkpalmaps1[0])[0])+0x00, 3UL ) ;
		set_data2(vidbase+(((UWORD)k)<<5L), modpaltitle,20L) ;
	}           

	VBK_REG=0 ;


	DISPLAY_ON ;




	while ( 1 )
	{

		dlcv = 0 ;          


		if ( LCDC_REG&0x08 )
		{
			vidbase = (unsigned char*)0x9800 ;
			set_bkg_data2(  0, 252U, walkframes1[dlcv] );
			addpal = 0 ;
		}
		else
		{
			vidbase = (unsigned char*)0x9C00 ;
			VBK_REG = 1 ;
			set_bkg_data2(  0, 252U, walkframes1[dlcv] );
			VBK_REG = 0 ;
			addpal = 8 ;

		}




		VBK_REG = 1;  // select palette bank 



		for ( k = 0 ; k != 18 ; k++ )
		{
			for ( j = 0 ; j != 14 ; j++ )
			{
				modpaltitle[j+3] = ((walkpalmaps1[dlcv])[k*14 + j]) + addpal ;
			}

			//memset( modpaltitle, addpal, 3UL ) ;
			//memset( modpaltitle+17UL, addpal, 3UL ) ;
			//		memset( modpaltitle, ((walkpalmaps1[dlcv])[0])+0x00, 3UL ) ;
			//memset( modpaltitle+17UL, ((walkpalmaps1[dlcv])[0])+0x00, 3UL ) ;
			set_data2(vidbase+(((UWORD)k)<<5L), modpaltitle,20L) ;
		}           



		VBK_REG = 0;  // select palette bank 

		while ( LY_REG != 143U ) ;

		set_bkg_palette( 0, 8, walkpals[dlcv] );


		if ( LCDC_REG&0x08 )
			LCDC_REG &= 0xF7 ;	//select $9800-$9BFF
		else
			LCDC_REG |= 0x08 ;	//select $9C00-$9FFF


		if ( joypad() & J_START )
			break ;


		remoteCall( 43, 44, title_animate2 ) ;

		if ( finishtitle != 0 )
			break ;

		remoteCall( 43, 45, title_animate3 ) ;


		if ( finishtitle != 0 )
			break ;

		remoteCall( 43, 46, title_animate4 ) ;

		if ( finishtitle != 0 )
			break ;

		remoteCall( 43, 47, title_animate5 ) ;


		if ( finishtitle != 0 )
			break ;

		remoteCall( 43, 48, title_animate6 ) ;


		if ( finishtitle != 0 )
			break ;


	}

	waitpadup() ;

	LCDC_REG |= 0x08 ;	//select $9C00-$9FFF

}
